#!/usr/bin/env node
// Regenerates the `_all` meta-component entry in registry.json so its registryDependencies
// list matches every other component currently in the registry. Run before `shadcn build`.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const registryPath = resolve(__dirname, '..', 'registry.json');

const META_NAME = '_all';

const registry = JSON.parse(readFileSync(registryPath, 'utf8'));

const componentNames = registry.items
  .filter((item) => item.name !== META_NAME)
  .map((item) => item.name)
  .sort();

const metaItem = {
  name: META_NAME,
  type: 'registry:ui',
  title: 'All Components',
  description:
    'Meta-component that installs the entire pandaworks-ui registry via registryDependencies. Auto-generated by scripts/sync-all-meta.mjs — do not edit by hand.',
  dependencies: [],
  registryDependencies: componentNames,
  files: [
    {
      path: 'registry/default/_all/_all.ts',
      type: 'registry:ui',
    },
  ],
};

const otherItems = registry.items.filter((item) => item.name !== META_NAME);
registry.items = [...otherItems, metaItem];

writeFileSync(registryPath, JSON.stringify(registry, null, 2) + '\n');

console.log(`[sync-all-meta] _all updated with ${componentNames.length} registryDependencies`);
